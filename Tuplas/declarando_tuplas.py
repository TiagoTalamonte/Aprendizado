# Tupla com vários elementos
tupla1 = (10, 20, 30, 40)
print(tupla1)  

# Tupla com elementos de tipos diferentes
tupla2 = ('maçã', 3.14, True)
print(tupla2) 

# Tupla com um único elemento (a vírgula é obrigatória!)
tupla3 = (5,)
print(tupla3)  

# Tupla vazia
tupla_vazia = ()
print(tupla_vazia)  
