saldo = 300
print(f'Saldo inicial: {saldo}')

saldo -= 5  # subtrair 5 do valor atual de saldo
print(f'Saldo após subtrair 5: {saldo}')

saldo //= 2  # divisão inteira de saldo por 2 (descarta a parte decimal)
print(f'Saldo após divisão inteira por 2: {saldo}')

saldo *= 2  # multiplicar o valor de saldo por 2
print(f'Saldo após multiplicar por 2: {saldo}')

saldo += 10  # somar 10 ao valor de saldo
print(f'Saldo após somar 10: {saldo}')