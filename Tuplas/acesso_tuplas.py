# Definindo uma tupla
tupla = ('maçã', 'banana', 'laranja', 'uva')

# Acessando o primeiro elemento (índice 0)
print(tupla[0])  

# Acessando o terceiro elemento (índice 2)
print(tupla[2])  

# Acessando o último elemento com índice negativo
print(tupla[-1])  

# Acessando um intervalo (slice) da tupla
print(tupla[1:3]) 